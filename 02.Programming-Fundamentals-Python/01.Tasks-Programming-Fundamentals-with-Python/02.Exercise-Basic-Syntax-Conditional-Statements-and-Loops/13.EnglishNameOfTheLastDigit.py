number = int(input())
last_number = number % 10

if last_number == 1:
    print("one")
elif last_number == 2:
    print("two")
elif last_number == 3:
    print("three")
elif last_number == 4:
    print("four")
elif last_number == 5:
    print("five")
elif last_number == 6:
    print("six")
elif last_number == 7:
    print("seven")
elif last_number == 8:
    print("eight")
elif last_number == 9:
    print("nine")
else:
    print("zero")
